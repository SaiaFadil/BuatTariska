    public void kode_barangg() {
        try {
            String sql = "SELECT id_produk FROM data_barang order by id_produk desc";
            Connection conn = (Connection) Koneksi.koneksi.getKoneksi();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet rs = stm.executeQuery(sql);

            if (rs.next()) {
                String Idno = rs.getString("id_produk").substring(2);
                String AN = "" + (Integer.parseInt(Idno) + 1);
                String Nol = "";
                if (AN.length() == 1) {
                    Nol = "0";
                } else if (AN.length() == 2) {
                    Nol = "";

                }

                txtkode.setText("BM" + Nol + AN);
            } else {
                txtkode.setText("BM01");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

