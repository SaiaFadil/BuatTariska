    private void btnubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnubahActionPerformed
        String kode_barang = tabelbahan.getValueAt(this.tabelbahan.getSelectedRow(), 0).toString();

        String nama = txtnama.getText();
        try {
            java.sql.Statement statement = Koneksi.koneksi.getKoneksi().createStatement();
            statement.executeUpdate(String.format("UPDATE data_barang"
                    + " SET nama_produk = '%s',"
                    + "WHERE id_produk = '%s'",
                    nama,  kode_barang));
            JOptionPane.showMessageDialog(null, "Data Berhasil Di Ubah");
            kode_barangg();
            txtnama.setText(null);
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Data Gagal Di Ubah" + e.getMessage());

        }
        tabelbahan();
        // TODO add your handling code here:
    }//GEN-LAST:event_btnubahActionPerformed

    private void btnhapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnhapusActionPerformed

        try {
            int jawab = JOptionPane.showOptionDialog(this,
                    "Ingin Hapus Data??",
                    "PILIH!",
                    JOptionPane.YES_NO_OPTION,
                    JOptionPane.QUESTION_MESSAGE, null, null, null);

            if (jawab == JOptionPane.YES_OPTION) {
                String kode_barang = tabelbahan.getValueAt(this.tabelbahan.getSelectedRow(), 0).toString();
                java.sql.Statement statement = Koneksi.koneksi.getKoneksi().createStatement();
                statement.executeUpdate("DELETE FROM data_barang where id_produk=('" + kode_barang + "');");
                JOptionPane.showMessageDialog(this, "Data Berhasil Di Hapus");
                kode_barangg();
                txtnama.setText("");
                txtkode.requestFocus();
                kode_barangg();
                tabelbahan();
            } else {
                JOptionPane.showMessageDialog(null, "Data Gagal Di HAPUS");
                kode_barangg();
                txtnama.setText("");
                txtkode.requestFocus();
                tabelbahan();

            }

        } catch (Exception e) {
            e.printStackTrace();

        }
        tabelbahan();
        // TODO add your handling code here:
    }//GEN-LAST:event_btnhapusActionPerformed

