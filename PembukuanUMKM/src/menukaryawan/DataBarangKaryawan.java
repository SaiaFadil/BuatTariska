  private void showData() {
        try {
            String kode_barang = loadtable.getValueAt(this.loadtable.getSelectedRow(), 0).toString();
            Connection conn = (Connection) Koneksi.koneksi.getKoneksi();
            Statement s = conn.createStatement();
            ResultSet r = s.executeQuery("SELECT id_bahan, nama_bahan ,stok_bahan FROM data_bmentah WHERE id_bahan = '" + kode_barang + "'");
            if (r.next()) {
                
               this.txtKBarang.setText(r.getString("id_bahan"));
                this.txtNBarang.setText(r.getString("nama_bahan"));
                this.txtSBarang.setText(r.getString("stok_bahan"));
            }
        } catch (SQLException | ClassNotFoundException ex) {
            ex.printStackTrace();
        }
    }

