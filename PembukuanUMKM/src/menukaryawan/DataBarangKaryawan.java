    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        String kode_barang = loadtable.getValueAt(this.loadtable.getSelectedRow(), 0).toString();

        String nama = txtNBarang.getText();
String stok = txtSBarang.getText();
        try {
            java.sql.Statement statement = Koneksi.koneksi.getKoneksi().createStatement();
            statement.executeUpdate(String.format("UPDATE data_bmentah"
                    + " SET nama_bahan = '%s',"
                    + " stok_bahan = '%s' "
                    + "WHERE id_bahan= '%s'", 
                    nama,stok,kode_barang));
            JOptionPane.showMessageDialog(null, "Data Berhasil Di Ubah");
            kode_barangg();
            txtNBarang.setText("");
            txtSBarang.setText("");
         
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Data Gagal Di Ubah"+e.getMessage());

        }
        loadtable();
    }//GEN-LAST:event_btnUbahActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed

        try {
            int jawab = JOptionPane.showOptionDialog(this,
                "Ingin Hapus Data??",
                "PILIH!",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.QUESTION_MESSAGE, null, null, null);

            if (jawab == JOptionPane.YES_OPTION) {
                String kode_barang = loadtable.getValueAt(this.loadtable.getSelectedRow(), 0).toString();
                java.sql.Statement statement = Koneksi.koneksi.getKoneksi().createStatement();
                statement.executeUpdate("DELETE FROM data_bmentah where id_bahan=('" + kode_barang + "');");
                JOptionPane.showMessageDialog(this, "Data Berhasil Di Hapus");
                kode_barangg();
                txtNBarang.setText("");
                txtSBarang.setText("");
                txtKBarang.requestFocus();

                loadtable();
            } else {
                JOptionPane.showMessageDialog(null, "Data Gagal Di HAPUS");
                kode_barangg();
                txtNBarang.setText("");
                txtSBarang.setText("");
                txtKBarang.requestFocus();
                loadtable();
            }

        } catch (Exception e) {
            e.printStackTrace();

        }
        loadtable();

    }//GEN-LAST:event_btnHapusActionPerformed

    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        String kode = txtKBarang.getText();
        String nama = txtNBarang.getText();
        String setok = txtSBarang.getText();

        try {
            java.sql.Statement statement = Koneksi.koneksi.getKoneksi().createStatement();
            statement.executeUpdate("insert into data_bmentah VALUES ('" + kode + "','" + nama + "','" + setok + "');");
            statement.close();
            JOptionPane.showMessageDialog(null, "Data Berhasil Ditambahkan");

            txtNBarang.setText("");
            txtSBarang.setText("");
            txtKBarang.requestFocus();
            kode_barangg();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Data Gagal Ditambahkan\n"
                    + "Jangan iseng ya :)");
        }
        loadtable();

    }//GEN-LAST:event_btnSimpanActionPerformed

